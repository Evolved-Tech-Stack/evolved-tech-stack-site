particles.forEach(particle => {
    // Random animation delay and duration
    const delay = Math.random() * 2;
    const duration = 2 + Math.random() * 2;
    particle.style.animationDelay = `${delay}s`;
    particle.style.animationDuration = `${duration}s`;
});
